    private void jButton_agregar_autorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_agregar_autorActionPerformed
        String autCodigo, autNombre, autNotas;
        
        autCodigo = jTextField1_codigoautor.getText();
        autNombre = jTextField2_nombreautor.getText();
        autNotas = jTextField3_anotacionesautor.getText();
        
        if (autCodigo.length() != 0) {
            dialogo("Refrescar pantalla para agregar nuevos registros.");
        }
        else
        {
            if (autNombre.length() > 0){
                Autor aut = new Autor(autNombre, autNotas);
                int varVerifica;

                varVerifica = aut.agregar();
                
                if (varVerifica > 0){
                    dialogo("Registro agregado correctamente");
                    
                    try {
                        refrescaAutor();
                    } catch (Exception e) {
                        JOptionPane.showMessageDialog(null, e.toString());
                    }
                }
                else{
                    dialogo("Ocurrió un error al intentar agregar el registro, por favor verifique la información e intente de nuevo...");
                }
            }
        }
    }//GEN-LAST:event_jButton_agregar_autorActionPerformed
    

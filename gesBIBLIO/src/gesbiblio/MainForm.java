    private void preBtnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_preBtnAddActionPerformed
        String preCod, preIsbn, preUsu, preEst;
        
        preCod = preTxtCod.getText();
        preIsbn = preTxtIsbn.getText();
        preUsu = preTxtDpi.getText();
        preEst = preTxtUpr.getText();
        
        if (preCod.length() != 0) {
            dialogo("Refrescar pantalla para agregar nuevos registros.");
        }
        else
        {
            if (preIsbn.length() > 0 && preUsu.length() > 0 && "HABILITADO".equals(preEst)){
                Prestamo pre = new Prestamo(preCod, preIsbn, preUsu);
                int varVerifica;

                varVerifica = pre.agregar();
                
                if (varVerifica > 0){
                    dialogo("Registro agregado correctamente");
                    
                    try {
                        MuestraCodigoPrestamo();
                        String strCodPre = preTblPre.getValueAt(0, 0).toString();
                        JOptionPane.showMessageDialog(null, "Código de préstamo generado: " + strCodPre);
                        refrescaPrestamo();
                    } catch (Exception e) {
                        JOptionPane.showMessageDialog(null, e.toString());
                    }
                }
                else{
                    dialogo("Ocurrió un error al intentar agregar el registro, por favor verifique la información e intente de nuevo...");
                }
            }
            else {
                if (preEst.length() > 0){
                    dialogo("El usuario ya llegó a su límite de préstamos");
                }
                else
                    dialogo("Seleccione un usuario de la tabla de usuarios para verificarlo.");
            }
        }
    }//GEN-LAST:event_preBtnAddActionPerformed


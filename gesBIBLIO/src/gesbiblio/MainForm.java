    private void jButton_agregar_libroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_agregar_libroActionPerformed
        String libCodigo, libTitulo, libEdicion, libAnio, libIsbn, libAutor, libEditorial, libGenero;
        
        libCodigo = jTextField1_codigolibro.getText();
        libTitulo = jTextField2_titulo.getText();
        libEdicion = jTextField3_edicion.getText();
        libAnio = jTextField5_aniopublicacion.getText();
        libIsbn = jTextField_isbn_libro.getText();
        libAutor = jComboBox1_autorlibro.getSelectedItem().toString();
        libEditorial = jComboBox1_editorial_libro.getSelectedItem().toString();
        libGenero = jComboBox1_genero_libro.getSelectedItem().toString();
        
        if (libCodigo.length() != 0){
            dialogo("Refrescar pantalla para agregar nuevos registros.");
        }
        else
        {
            if (libTitulo.length() > 0 && libEdicion.length() > 0 && libAnio.length() > 0 && libIsbn.length() > 0 && 
                    !"[Seleccione]".equals(libAutor) && !"[Seleccione]".equals(libEditorial) && !"[Seleccione]".equals(libGenero)){
                Libro lib = new Libro(libTitulo, libAutor, libEdicion, libEditorial, libAnio, libIsbn, libGenero);
                
                int varVerifica;

                varVerifica = lib.agregar();
                
                if (varVerifica > 0){
                    dialogo("Registro agregado correctamente");
                    
                    try {
                        refrescaLibro();
                        refrescaPrestamo();
                    } catch (Exception e) {
                        JOptionPane.showMessageDialog(null, e.toString());
                    }
                }
                else{
                    dialogo("Ocurrió un error al intentar agregar el registro, por favor verifique la información e intente de nuevo...");
                }
            }
            else{
                dialogo("Completar la informacion del libro");
            }
        }
    }//GEN-LAST:event_jButton_agregar_libroActionPerformed

